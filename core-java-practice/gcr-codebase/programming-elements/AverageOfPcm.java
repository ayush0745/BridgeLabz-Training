
//to calculate average of pcm marks
public class AverageOfPcm {
    public static void main(String[] args) {
        int physics = 95;
        int chemistry = 96;
        int mathematics = 94;
        double average = (physics + chemistry + mathematics) / 3.0;
        System.out.println("The average marks for pcm is " + average);
    }
}
